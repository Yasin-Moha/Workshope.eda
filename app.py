from sklearn.ensemble import RandomForestClassifier
from sklearn.metrics import accuracy_score, precision_score, recall_score, f1_score, classification_report, confusion_matrix
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

# Convert continuous 'DubityMet' into a binary classification target
# Assuming 'DubityMet' > 0 means 'rain' (1) and 'DubityMet' == 0 means 'no rain' (0)
y_classification = (y > 0).astype(int)

# Split data with the new classification target
x_train_cls, x_test_cls, y_train_cls, y_test_cls = train_test_split(
    x,
    y_classification,
    test_size=0.2,
    random_state=42
)

# Scale the features (using the same scaler as before, or re-fit if necessary for this split)
# Re-initializing scaler to ensure it's fit on the classification training data if not explicitly done before
scaler_cls = StandardScaler()
x_train_cls_scaled = scaler_cls.fit_transform(x_train_cls)
x_test_cls_scaled = scaler_cls.transform(x_test_cls)

# Initialize and train a Random Forest Classifier
rf_classifier = RandomForestClassifier(n_estimators=100, random_state=42)
rf_classifier.fit(x_train_cls_scaled, y_train_cls)

# Make predictions on the test set
y_pred_cls = rf_classifier.predict(x_test_cls_scaled)

# Evaluate the classifier
print("\nRandom Forest Classifier Performance:")
print(f"Accuracy: {accuracy_score(y_test_cls, y_pred_cls):.4f}")
print(f"Precision: {precision_score(y_test_cls, y_pred_cls, zero_division=0):.4f}")
print(f"Recall: {recall_score(y_test_cls, y_pred_cls, zero_division=0):.4f}")
print(f"F1 Score: {f1_score(y_test_cls, y_pred_cls, zero_division=0):.4f}")

print("\nClassification Report:")
print(classification_report(y_test_cls, y_pred_cls, zero_division=0))

