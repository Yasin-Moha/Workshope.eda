"""### Hyperparameter Tuning with GridSearchCV

To improve the model's performance and potentially reduce overfitting, we can perform hyperparameter tuning using `GridSearchCV`. This method exhaustively searches over specified parameter values for an estimator and selects the best combination.
"""

from sklearn.model_selection import GridSearchCV

# Define the parameter grid to search
param_grid = {
    'n_estimators': [50, 100, 200],  # Number of trees in the forest
    'max_depth': [None, 10, 20, 30], # Maximum number of levels in tree
    'min_samples_split': [2, 5, 10], # Minimum number of samples required to split an internal node
    'min_samples_leaf': [1, 2, 4],   # Minimum number of samples required to be at a leaf node
    'max_features': ['sqrt', 'log2', None] # Number of features to consider when looking for the best split
}

# Initialize GridSearchCV
# We'll use the same RandomForestClassifier instance and classification data
grid_search = GridSearchCV(estimator=rf_classifier,
                           param_grid=param_grid,
                           cv=3,  # Using 3-fold cross-validation for speed, can increase for more robust results
                           n_jobs=-1, # Use all available cores
                           verbose=2, # Verbose output
                           scoring='accuracy') # Metric to optimize

# Fit GridSearchCV to the training data
grid_search.fit(x_train_cls_scaled, y_train_cls)

# Print the best parameters and best score
print(f"Best parameters found: {grid_search.best_params_}")
print(f"Best cross-validation accuracy: {grid_search.best_score_:.4f}")

# Get the best model
best_rf_classifier = grid_search.best_estimator_

# Evaluate the best model on the test set
y_pred_best = best_rf_classifier.predict(x_test_cls_scaled)

print("\nPerformance of the best Random Forest Classifier (after tuning):")
print(f"Accuracy: {accuracy_score(y_test_cls, y_pred_best):.4f}")
print(f"Precision: {precision_score(y_test_cls, y_pred_best, zero_division=0):.4f}")
print(f"Recall: {recall_score(y_test_cls, y_pred_best, zero_division=0):.4f}")
print(f"F1 Score: {f1_score(y_test_cls, y_pred_best, zero_division=0):.4f}")

print("\nClassification Report (tuned model):")
